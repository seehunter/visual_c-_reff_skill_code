/* 1.1.7 演示 continue的用法  
/************************************************************************/
#include <iostream.h>
int main()
{
   char* p = "this is Aa test ";
   while(*p)
   {
	   if(*p == 'A')
	   {   
		   p++;
		   continue;
	   }
	   cout<<*p;
	   p++;
   }
   cout<<endl;
   return 0;
}