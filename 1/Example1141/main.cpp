#include <iostream.h>
/************************************************************************/
/* 主函数
/************************************************************************/
int main()
{
   int m = 3;
   int n = 2;//声明并且初始化临时变量

   if(--m || --n)
   {
	   cout<<"m="<<m<<endl;
	   cout<<"n="<<n<<endl;
   }
   return 0;
}
