#include <iostream.h>
/************************************************************************/
/* 函数名       : Function1
/* 参数1 nCount : 计数
/* 函数作用     :  
/************************************************************************/
int  Function1( int nCount)
{
    int i = 0;
	int nRet = 0;
//空行
	for( i=1; i<nCount+1; i++)
	{
       nRet += i;
	}

    cout<<nRet<<endl;
	
	return 0;
}
/************************************************************************/
/* 主函数
/************************************************************************/
int main()
{
	int nTmpCount = 100;

	Function1(nTmpCount);

	return 0;	
}