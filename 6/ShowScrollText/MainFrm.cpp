void CMainFrame::OnTimer(UINT nIDEvent) 
{
	///////////////////////////////////////////////////////////////////
	//在状态栏上显示滚动文本
	CString strText,strLeft,strRight;
    int nLen;
    strText=m_wndStatusBar.GetPaneText(2);//获得状态栏第3个面板的显示字符
    nLen=strText.GetLength();              
    strRight=strText.Left(2);
    strLeft=strText.Right(nLen-2);
    strText=strLeft+strRight;
    m_wndStatusBar.SetPaneText(2,strText);//设置状态栏第3个面板的显示字符
	//
    ///////////////////////////////////////////////////////////////////
	CFrameWnd::OnTimer(nIDEvent);
}
